                <div class="alert alert-success"><?php echo $success; ?></div>
            <?php endif; ?>
            
            <?php if ($page == 'register'): ?>
                <!-- Registration Form -->
                <h2 class="form-title">Create an Account</h2>
                <form method="post" action="index.php?page=register" id="registrationForm">
                    <div class="mb-3">
